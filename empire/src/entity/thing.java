package entity;

public class thing {
	public int id;
	public String name;
	public String time;
	
	public int getid(){
		return id;
	}
	public void setid(int id){
		this.id=id;
	}
	
	public String getname(){
		return name;
	}
	public void setname(String name){
		this.name=name;
	}
	
	public String gettime(){
		return time;
	}
	public void settime(String time){
		this.time=time;
	}

}
